#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	nbr;
	char	*c;
	int		i;

	nbr = n;
	c = (char *)s;
	i = 0;
	while (i < nbr)
	{
		c[i] = 0;
		i++;
	}
}
