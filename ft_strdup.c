#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		t;
	char	*dest;

	i = 0;
	t = ft_strlen(s1);
	dest = (char *)malloc(sizeof(char) * (t + 1));
	if (!(dest))
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
